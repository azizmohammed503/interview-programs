package com.core;

public class Switch {
	public static void main(String a[]){
		int price = 2;
		switch (price) {
		
			case 2: System.out.println("It is: 2");
			break;
			case 5: System.out.println("It is: 5");
			default: System.out.println("It is: default");
			break;
			case 9: System.out.println("It is: 9");
		}
	}
}
